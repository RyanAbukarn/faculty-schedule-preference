    public String register(UserInput request)
            throws IllegalStateException, UnsupportedEncodingException, MessagingException {
        boolean isValidEmail = emailValidator
                .test(request.getEmail());

        if (!isValidEmail) {
            throw new IllegalStateException("email is not valid");
        }

        if (!(request.getPassword().equals(request.getConfirmPassword()))) {
            throw new IllegalStateException("Passwords do not match");
        }

        // I assume only one department is added at the signup page...
        Department department = departmentRepository.findById(request.getDepartment()).get();

        String token = myUserDetailsService.signUpUser(
                new User(
                        request.getCsunID(),
                        request.getName(),
                        request.getUsername(),
                        request.getEmail(),
                        request.getPassword(),
                        department));

        String link = "http://localhost:3001/users/" + token + "/confirm";
        emailSender.send(request.getEmail(), buildEmail(request.getName(), link));

        return token;
    }

    public String getSiteURL(HttpServletRequest request) {
        String siteURL = request.getRequestURL().toString();
        return siteURL.replace(request.getServletPath(), "");
    }

    @Transactional
    public void confirmToken(String token) throws IllegalStateException {
        ConfirmationToken confirmationToken = confirmationTokenService.getToken(token)
                .orElseThrow(() -> new IllegalStateException("Token not found"));
        if (confirmationToken.getConfirmedAt() != null) {
            throw new IllegalStateException("Email is already confirmed");
        }

        LocalDateTime expiredAt = confirmationToken.getExpiresAt();

        if (expiredAt.isBefore(LocalDateTime.now())) {
            throw new IllegalStateException("Token expired");
        }

        confirmationToken.setConfirmedAt(LocalDateTime.now());
        confirmationToken.getUser().setEnabled(true);
    }

    public void updateResetPasswordToken(String token, String email) throws IllegalStateException {
        User user = userRepository.getByEmail(email);

        if (user != null) {
            user.setResetPasswordToken(token);
            userRepository.save(user);
        } else {
            throw new IllegalStateException("Could not find any user with the email" +
                    email);
        }
    }

    public User getByResetPasswordToken(String token) {
        return userRepository.findByResetPasswordToken(token);
    }

    public void updatePassword(User user, String newPassword) {
        String encodedPassword = passwordEncoder.encode(newPassword);
        user.setPassword(encodedPassword);

        user.setResetPasswordToken(null);
        userRepository.save(user);
    }

